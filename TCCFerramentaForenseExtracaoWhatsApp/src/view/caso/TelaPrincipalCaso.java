    private void carregaJTreeDispositivos(){
        DefaultMutableTreeNode titulo = new DefaultMutableTreeNode(casoSelecionado.getDescricao());

        if(dispositivos.isEmpty()){
            DefaultMutableTreeNode dispositivo[] = new DefaultMutableTreeNode[1];
            dispositivo[0] = new DefaultMutableTreeNode("Sem dispositivos");
            titulo.add(dispositivo[0]);
        }
        else{
            for (int i = 0; i < dispositivos.size(); i++) {
                DefaultMutableTreeNode dispositivo[] = new DefaultMutableTreeNode[dispositivos.size()];
                dispositivo[i] = new DefaultMutableTreeNode(dispositivos.get(i));
                titulo.add(dispositivo[i]);
            }
        }
        
        arvoreDispositivos.setModel(new DefaultTreeModel(titulo));
    }
    
    
    private void preencheTabelasCasos(String filtro){
        CasoDAO casoDAO = new CasoDAO();
        if(filtro.equals("TUDO")){
            casos = casoDAO.pesquisaListaCasos(usuarioLogado, TipoAcesso.Criador);
            casosCompartilhados = casoDAO.pesquisaListaCasos(usuarioLogado, TipoAcesso.Visualizar);
        }
        if(filtro.equals("NOME")){
            casos = casoDAO.pesquisaListaCasosPorNome(usuarioLogado, TipoAcesso.Criador, campoPesquisaCaso.getText());
            casosCompartilhados = casoDAO.pesquisaListaCasosPorNome(usuarioLogado, TipoAcesso.Visualizar, campoPesquisaCaso.getText()); 
        }
        tabelaMeusCasos.setModel(new TabelaModeloCaso(casos));
        tabelaCasosCompartilhados.setModel(new TabelaModeloCaso(casosCompartilhados));
    }
    
    
